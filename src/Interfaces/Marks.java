    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed

        sid = SID.getText();
        e_key = E_Key.getSelectedItem().toString();
        
        if ( sid == "" ) {
            
            JOptionPane.showMessageDialog ( null, "1Invalid Student Id or E_Key." );
        }
        
        else {
        try {
            
            sql = "SELECT Std_id, E_key, Marks FROM marks WHERE  Std_id = '"+ sid +"' OR E_key='"+ e_key +"' ";
            pst = con.prepareStatement ( sql );
            rs =  pst.executeQuery();
               
            Stu_marks.setModel ( DbUtils.resultSetToTableModel ( rs ));

        }

        catch ( Exception e ) {

            JOptionPane.showMessageDialog ( null, "Invalid Student Id or E_Key." );
            
            System.out.println ( e );
 
        }
        }
    }//GEN-LAST:event_SearchActionPerformed

