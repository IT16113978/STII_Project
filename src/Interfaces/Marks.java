    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed

        sid = SID.getText();
        e_key = E_Key.getSelectedItem().toString();
        marks = Integer.parseInt ( Marks.getText());

        if ( marks <= 100 && marks >= 0 ) {
        
            if ( marks >= 90 ) {
                
                grade = "A+";
                
            }
            
            else if ( marks >= 80 ) {
                
                grade = "A";
                
            }
            
            else if ( marks >= 75 ) {
                
                grade = "A-";
                
            }
            
            else if ( marks >= 70 ) {
                
                grade = "B+";
                
            }
            
            else if ( marks >= 65 ) {
                
                grade = "B";
                
            }
            
            else if ( marks >= 60 ) {
                
                grade = "B-";
                
            }
            
            else if ( marks >= 55 ) {
                
                grade = "C+";
                
            }
            
            else if ( marks >= 45 ) {
                
                grade = "C";
                
            }
            
            else if ( marks >= 40 ) {
                
                grade = "C-";
                
            }
            
            else {
                
                grade = "E";
                
            }
            
            try {

            sql = "INSERT INTO marks ( Std_id, E_key, Marks, Grade ) VALUES ( '"+ sid +"', '"+ e_key +"', '"+ marks +"', '"+ grade +"')";
            pst = con.prepareStatement ( sql );
            pst.execute ();
            
            JOptionPane.showMessageDialog ( null, "Save Succesful" );
            
            tableload ();

            }

            catch ( Exception e ) {
            
                JOptionPane.showMessageDialog ( null, "Records are incomplete." );
                
                System.out.println ( e );
 
            }
        }
            
        else {
                
            JOptionPane.showMessageDialog ( null, "Invalid Mark." );
                
        }
    
    }//GEN-LAST:event_AddActionPerformed

